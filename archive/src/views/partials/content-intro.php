<article id="section-intro">
    <h1>Introduction</h1>

    <section>
        <p>Aegaeon is an OpenID Connect / OAuth 2.0 server implementation using spring boot. The project was originally inspired by mitreid-connect.</p>
        <p>The code is available under Apache 2.0 license and is currently under heavy development.</p>
        <p>The project was started with the goal of having a better understanding of Openid / OAuth standard and has evolved to a project with the following goal :</p>
        <ul>
            <li>Creating an <i>opiniatre</i> implementation. i.e. Having sensible default and being pragmatic.</li>
            <li>Simple, fast and clean</li>
            <li>Being up-to-date</li>
        </ul>

        <p>
            <h2>Features (completed and planned)</h2>
            <ul>
                <li>Implicit flow (completed)</li>
                <li>Authorization Code (completed)</li>
                <li>Client Credential (ongoing)</li>
                <li>Full error handling (ongoing)</li>
                <li>User info endpoint (completed)</li>
                <li>Introspect endpoint (completed)</li>
                <li>JWT using RSA signature (completed)</li>
                <li>JWT using HMAC Protection (completed)</li>
                <li>Clustering ready (ongoing)</li>
                <li>Basic Client administration</li>
                <li>API Scopes / Claims centric</li>
            </ul>
            <h2>Notable Technologies</h2>
            <ul>
                <li>Spring Framework 4.3.10</li>
                <li>Spring Boot 1.5.6</li>
                <li>Hibernate 5.0.12</li>
                <li>Thymeleaf 3.0.7</li>
                <li>Nimbus 5.1</li>
            </ul>
        </p>
    </section>
